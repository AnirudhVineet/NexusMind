"""
Retrieval evaluation harness using ranx.

Usage:
    cd backend && python -m tests.retrieval_eval

Requires:
    pip install ranx

The fixture file `tests/fixtures/retrieval_qrels.json` contains hand-labelled
query-chunk relevance pairs in the format:
    {
      "queries": {"q1": "what is attention mechanism?", ...},
      "qrels":   {"q1": {"chunk_uuid_1": 1, "chunk_uuid_2": 2}, ...}
    }

Relevance grades: 0=not relevant, 1=relevant, 2=highly relevant.

Run after Phase 3.1 is applied to record the baseline NDCG@10 and MRR@10.
Save output to docs/phase3/retrieval_baseline.md.
"""
from __future__ import annotations

import asyncio
import json
import os
import sys
from pathlib import Path

FIXTURE_PATH = Path(__file__).parent / "fixtures" / "retrieval_qrels.json"
DOCS_PATH = Path(__file__).parents[2] / "docs" / "phase3" / "retrieval_baseline.md"


def _load_fixture() -> tuple[dict, dict]:
    if not FIXTURE_PATH.exists():
        print(
            f"[retrieval_eval] Fixture not found at {FIXTURE_PATH}.\n"
            "Create it with hand-labelled query-chunk pairs to run evaluation.\n"
            "Example schema:\n"
            '  {"queries": {"q1": "what is X?"}, "qrels": {"q1": {"<chunk_uuid>": 2}}}'
        )
        return {}, {}
    data = json.loads(FIXTURE_PATH.read_text())
    return data.get("queries", {}), data.get("qrels", {})


async def _run_retrieval(queries: dict) -> dict:
    """Run hybrid_search for each query and return {qid: {chunk_id: rank_score}}."""
    try:
        from ranx import Qrels, Run, evaluate  # noqa: F401
    except ImportError:
        print("[retrieval_eval] Install ranx: pip install ranx")
        return {}

    # Import here to avoid circular issues during plain test collection
    from sqlalchemy.ext.asyncio import async_sessionmaker, create_async_engine

    from app.core.config import get_settings
    from app.services.embedding import get_embedding_service
    from app.services.retrieval import hybrid_search

    settings = get_settings()
    engine = create_async_engine(settings.database_url, echo=False, future=True)
    Session = async_sessionmaker(engine, expire_on_commit=False)

    run_data: dict = {}
    embedder = get_embedding_service()

    # We need a real user_id that owns the indexed chunks.
    # This eval script expects DATABASE_URL to point at a populated DB.
    # Override USER_ID env var to specify whose chunks to query.
    user_id_str = os.environ.get("EVAL_USER_ID", "")
    if not user_id_str:
        print(
            "[retrieval_eval] Set EVAL_USER_ID env var to the UUID of a user "
            "whose documents are indexed in the database."
        )
        await engine.dispose()
        return {}

    import uuid

    user_id = uuid.UUID(user_id_str)

    async with Session() as session:
        for qid, query_text in queries.items():
            vec = await embedder.embed_query(query_text)
            hits = await hybrid_search(
                session=session,
                user_id=user_id,
                query=query_text,
                query_vec=vec,
                top_k=10,
            )
            run_data[qid] = {str(h.chunk_id): float(len(hits) - i) for i, h in enumerate(hits)}

    await engine.dispose()
    return run_data


def main() -> None:
    try:
        from ranx import Qrels, Run, evaluate
    except ImportError:
        print("[retrieval_eval] Install ranx: pip install ranx")
        sys.exit(1)

    queries, qrels_data = _load_fixture()
    if not queries:
        sys.exit(0)

    run_data = asyncio.run(_run_retrieval(queries))
    if not run_data:
        sys.exit(1)

    qrels = Qrels(qrels_data)
    run = Run(run_data, name="nexusmind-hybrid")

    metrics = evaluate(qrels, run, ["ndcg@10", "mrr@10", "precision@5"])
    print("\n=== NexusMind Retrieval Evaluation ===")
    for metric, value in metrics.items():
        print(f"  {metric}: {value:.4f}")

    DOCS_PATH.parent.mkdir(parents=True, exist_ok=True)
    baseline_md = (
        "# Retrieval Baseline — Phase 3.1\n\n"
        "| Metric | Value |\n|---|---|\n"
    )
    for metric, value in metrics.items():
        baseline_md += f"| {metric} | {value:.4f} |\n"
    baseline_md += "\n_Generated by `tests/retrieval_eval.py`_\n"
    DOCS_PATH.write_text(baseline_md)
    print(f"\nBaseline saved to {DOCS_PATH}")


if __name__ == "__main__":
    main()
